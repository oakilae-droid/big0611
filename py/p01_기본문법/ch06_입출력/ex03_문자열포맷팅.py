# 1세대: % 포맷팅 (파이썬 2 이전부터 사용)
name = "철수"
age = 25

# 1. 기본 사용
print("이름: %s, 나이: %d세" % (name, age))
# 출력: 이름: 철수, 나이: 25세

# 2. 실수 소수점 제한 (소수점 아래 2자리까지 표기)
pi = 3.141592
print("원주율: %.2f" % pi)
# 출력: 원주율: 3.14


# 2세대: str.format 매서드 (파이썬 3.0부터 도입)
name = "영자"
age = 23

# 1. 기본 순서대로 매칭
print("이름: {}, 나이: {}세".format(name, age))

# 2. 인덱스 번호(0부터 시작) 지정하여 순서 바꾸기
print("나이: {1}세, 이름: {0}".format(name, age))
# 출력: 나이: 23세, 이름: 영자

# 3. 실수 소수점 제한 (콜론뒤에 .2f 표기)
pi = 3.141592
print("원주율: {:.2f}".format(pi))


# 3세대: f-string (파이썬 3.6부터 도입 ~ 현재 표준)
name = "minsu"
age = 27

# 1. 기본 사용 (직관성의 끝판왕)
print(f"이름: {name}, 나이: {age}세")

# 2. 중괄호 안에서 간단한 연산이나 함수 호출도 가능
print(f"내년 나이: {age + 1}세, 이름 대문자로: {name.upper()}")

# 3. 실수 소수점 제한 및 천 자릿수 콤마(,) 표기
money = 10000000
pi = 3.141592
print(f"원주율: {pi:.2f}")     # 출력: 원주율: 3.14
print(f"잔고: {money:,}원")   # 출력: 잔고: 10,000,000원